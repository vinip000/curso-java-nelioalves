package estruturaSequencial.Exercicios;

public class exercicio1 {
    public static void main(String[] args) {
        System.out.println("Exercício 1 - Estrutura Sequencial");

        int a = 10;
        int b = 30;
        int soma = a + b;

        System.out.println("A soma entre " + a + " e " + b + " é igual a " + soma);

        int c = -30;
        int d = 10;
        int soma2 = c + d;
        System.out.println("A soma entre " + c + " e " + d + " é igual a " + soma2);

        int e = 0;
        int f = 0;
        int soma3 = e + f;
        System.out.println("A soma entre " + e + " e " + f + " é igual a " + soma3);
    }
}
